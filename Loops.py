# Loops in Python

# for, while

for i in range(1,10,2) :
    print(i)

# step is by default '1'

for i in range(1,11) :
    print("Hello")

print("\n")

i = 1
while i < 11 :
    print("Hello")
    i = i + 1

for _ in range(20) :
    print("Pranjal")

# looping for a list 

list = [10,20,30,40,50,60]

for i in list :
    print(i)

friends = ["niraj","aman","kkp","mukesh"]

for i in friends :
    print(i)

